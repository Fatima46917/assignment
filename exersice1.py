if age >= 80:
    print("The ticket price is 25$")
elif age >= 60:
    print("The ticket price is 20$")
elif age >= 40:
    print("The ticket price is 15$")
elif age >= 20:
    print("The ticket price is 10$")
else:
    print("The ticket price is 5$")

 
